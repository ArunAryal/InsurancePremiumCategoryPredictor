#import ml model
with open('pipeline.pkl','rb') as f:
    my_pipeline=pickle.load(f)


app=FastAPI()

tier_1_cities = ["Mumbai", "Delhi", "Bangalore", "Chennai", "Kolkata", "Hyderabad", "Pune"]
tier_2_cities = ["Jaipur", "Chandigarh", "Indore", "Lucknow", "Patna", "Ranchi", "Visakhapatnam", "Coimbatore",
    "Bhopal", "Nagpur", "Vadodara", "Surat", "Rajkot", "Jodhpur", "Raipur", "Amritsar", "Varanasi",
    "Agra", "Dehradun", "Mysore", "Jabalpur", "Guwahati", "Thiruvananthapuram", "Ludhiana", "Nashik",
    "Allahabad", "Udaipur", "Aurangabad", "Hubli", "Belgaum", "Salem", "Vijayawada", "Tiruchirappalli",
    "Bhavnagar", "Gwalior", "Dhanbad", "Bareilly", "Aligarh", "Gaya", "Kozhikode", "Warangal",
    "Kolhapur", "Bilaspur", "Jalandhar", "Noida", "Guntur", "Asansol", "Siliguri"]


# pydantic model to validate incoming data
class UserInput(BaseModel):
    age:Annotated[int,Field(...,gt=0,lt=120,description='Age of the user')]
    weight:Annotated[float,Field(...,gt=0,description='Weight of the user in kg')]
    height:Annotated[float,Field(...,gt=0,lt=2.5,description='height of the user in meters')]
    income_lpa:Annotated[float,Field(...,gt=0,description='Salary of the user in lpa')]  
    smoker:Annotated[bool,Field(...,description='smoking status of the user')]
    city:Annotated[str,Field(...,description='City of the user')]
    occupation:Annotated[Literal['retired', 'freelancer', 'student', 'government_job',
    'business_owner', 'unemployed', 'private_job'],Field(...,description='Age of the user')]


    @computed_field
    @property
    def bmi(self) -> float:
        return round(self.weight / self.height**2, 2)
    
    @computed_field
    @property
    def life_style_risk(self)->str:
        if self.smoker and self.bmi>30:
            return 'high'
        elif self.smoker or self.bmi >27:
            return 'medium'
        else:
            return 'low'
        
    @computed_field
    @property
    def city_tier(self) ->int:
        if self.city in tier_1_cities:
            return 1
        elif self.city in tier_2_cities:
            return 2
        else:
            return 3


    @computed_field
    @property
    def age_group(self)->str:
        if self.age<25:
            return 'young'
        elif self.age<45:
            return 'adult'
        elif self.age<60:
            return 'middle_aged'
        return 'senior'
    
#crating a route
@app.post('/predict')
def predict_premium(data:UserInput):

    input_df= pd.DataFrame([
        {
        'income_lpa':data.income_lpa,
        'occupation':data.occupation,
        'bmi':data.bmi,
        'age_group':data.age_group,
        'life_style_risk':data.life_style_risk,
        'city_tier':data.city_tier
        }]
    )

    prediction= my_pipeline.predict(input_df)[0]

    return JSONResponse(status_code=200, content={'response': {'predicted_category': prediction}})